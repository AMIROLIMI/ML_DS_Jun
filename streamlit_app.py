    st.markdown(f"üìä –¢–æ—á–Ω–æ—Å—Ç—å –ª–æ–≥–∏—Å—Ç–∏—á–µ—Å–∫–æ–π —Ä–µ–≥—Ä–µ—Å—Å–∏–∏: {log_reg_acc:.4f}")
    st.markdown(f"üìä –¢–æ—á–Ω–æ—Å—Ç—å –¥–µ—Ä–µ–≤–∞ —Ä–µ—à–µ–Ω–∏–π: {dtc_acc:.4f}")

